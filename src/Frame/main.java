    String tienerecursividad(String p) {
        String cab = p.substring(0, 1);
        String[] prods = p.split("\\|");
        int i = 0;
        String r = "";
        while (prods.length > i) {
            if (i == 0) {
                if (prods[i].substring(3, 4).equals(cab)) {
                    r = i + "_";
                }
                i++;
            } else {
                if (prods[i].substring(0, 1).equals(cab)) {
                    r = r + i + "_";
                }
                i++;
            }
        }
        if (!"".equals(r)) {
            return r;
        } else {
            return "-1";
        }
    }

